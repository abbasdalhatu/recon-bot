async def whois_socket_lookup(target, detailed=False):
    whois_servers = {
        'com': 'whois.verisign-grs.com', 'net': 'whois.verisign-grs.com',
        'org': 'whois.pir.org', 'info': 'whois.afilias.net',
        'io': 'whois.nic.io', 'co': 'whois.nic.co',
        'me': 'whois.nic.me', 'xyz': 'whois.nic.xyz',
        'dev': 'whois.nic.google', 'app': 'whois.nic.google',
        'uk': 'whois.nic.uk', 'de': 'whois.denic.de',
        'fr': 'whois.nic.fr', 'br': 'whois.registro.br',
        'ai': 'whois.nic.ai', 'tech': 'whois.nic.tech',
        'cloud': 'whois.nic.cloud', 'io': 'whois.nic.io',
    }
    tld = target.split('.')[-1]
    server = whois_servers.get(tld, 'whois.verisign-grs.com')
    try:
        sock = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
        sock.settimeout(10)
        sock.connect((server, 43))
        sock.send((target + "\r\n").encode())
        resp = b""
        while True:
            d = sock.recv(4096)
            if not d:
                break
            resp += d
        sock.close()
        text = resp.decode('utf-8', errors='ignore')
        relevant = [l.strip() for l in text.split('\n') 
                    if any(x in l.lower() for x in ['registrar', 'creation date', 'expir', 'name server',
                                                     'org', 'country', 'admin email', 'registrant', 'dnssec',
                                                     'domain name', 'status:', 'dnssec'])]
        if relevant:
            return "=== [2] WHOIS ===\n" + "\n".join(f"  {l}" for l in relevant[:20])
        return "=== [2] WHOIS ===\n  No data returned from {server}"
    except Exception as e:
        return f"=== [2] WHOIS ===\n  Error: {str(e)}"


