async def send_long_message(chat_id, context, text, chunk_size=4000):
    lines = text.split('\n')
    chunks, current = [], ""
    for line in lines:
        if len(current) + len(line) + 1 > chunk_size:
            chunks.append(current)
            current = line
        else:
            current = (current + "\n" + line) if current else line
    if current:
        chunks.append(current)
    for chunk in chunks:
        try:
            await context.bot.send_message(chat_id, chunk)
            await asyncio.sleep(0.3)
        except:
            pass


