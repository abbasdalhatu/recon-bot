def get_certificate_info(hostname, port=443):
    try:
        ctx = ssl.create_default_context()
        ctx.check_hostname = False
        ctx.verify_mode = ssl.CERT_NONE
        with socket.create_connection((hostname, port), timeout=5) as sock:
            with ctx.wrap_socket(sock, server_hostname=hostname) as ssock:
                cert = ssock.getpeercert(binary_form=False)
                if not cert:
                    return None
                info = {}
                subject = dict(x[0] for x in cert.get('subject', []))
                info['cn'] = subject.get('commonName', 'N/A')
                info['org'] = subject.get('organizationName', 'N/A')
                info['country'] = subject.get('countryName', 'N/A')
                issuer = dict(x[0] for x in cert.get('issuer', []))
                info['issuer'] = issuer.get('commonName', 'N/A')
                info['valid_from'] = cert.get('notBefore', 'N/A')
                info['valid_to'] = cert.get('notAfter', 'N/A')
                sans = [ext[1] for ext in cert.get('subjectAltName', []) if ext[0] == 'DNS']
                info['sans'] = sans[:30]
                info['sans_count'] = len(sans)
                info['cipher'] = ssock.cipher()
                return info
    except:
        return None


